<script>


    $(function(){

        $( document ).on( 'keydown', function ( e ) {
            if ( e.keyCode === 27 ) {
                $('.modal-show-photo').fadeOut();
            }
        });



        $('.img-gal img').click(function(){
            var dataGal = $(this).data('gal');
            var dataPost = $(this).data('post');

            createSwiper(dataGal,dataPost);
            $('.modal-show-photo').fadeIn();
            var mySwiper = new Swiper ('.swiper-container', {
                // Optional parameters
                loop: true,
                effect:'fade',
                fade: {
                    crossFade: true
                },
                // Navigation arrows
                nextButton: '.swiper-button-next',
                prevButton: '.swiper-button-prev',
                pagination: '.swiper-pagination',
                paginationType: 'fraction'
            })
        });
    })
    function createSwiper(pp_gal,idPost){
        if(pp_gal == 'alone'){
            var img = $('img[data-gal="alone"][data-post="'+ idPost +'"]');
            $('.wrap-content-modal-photo .swiper-wrapper').html('');
            $('.swiper-button-next,.swiper-button-prev,.mini-desc-count-modal > div').hide();
            generateSlide(img);
        }
        if(pp_gal == 'pp_gallery'){
            var img = $('img[data-gal="pp_gallery"][data-post="'+ idPost +'"]');
            $('.wrap-content-modal-photo .swiper-wrapper').html('');
            $('.swiper-button-next,.swiper-button-prev,.mini-desc-count-modal > div').show();
            generateSlide(img);
        }
    }
    function generateSlide(img){
        img.each(function(){
            var imgSrc = $(this).attr('src');
            var slide = '<div class="swiper-slide">' + '<img src="'+ imgSrc +'" alt="test1">' + '</div>';
            $('.wrap-content-modal-photo .swiper-wrapper').append(slide);
        });
    }

</script>